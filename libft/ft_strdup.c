#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*dest;
	size_t	len;

	len = ft_strlen(src) + 1;
	dest = malloc(sizeof(char) * len);
	if (!dest)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
